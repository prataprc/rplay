pub fn run() {
    let input: Vec<i32> = [3, 4, -1, 1].to_vec();
    let ln = input.len() as i32;
    let mut scratch: Vec<u8> = Vec::with_capacity((ln as usize / 8) + 1);
    scratch.resize(scratch.capacity(), 0);
    input
        .into_iter()
        .filter_map(|x| if x > 0 && x <= ln { Some(x - 1) } else { None })
        .for_each(|x| {
            scratch[(x as usize) / 8] |= 1 << ((x as usize) % 8);
        });
    let pnum = match scratch
        .into_iter()
        .enumerate()
        .skip_while(|(_, x)| *x == 0xFF)
        .next()
    {
        Some((i, x)) => ((i as i32) * 8) + ((LOOKUP[x as usize]) as i32) + 1,
        None => -1,
    };
    println!("{}", pnum);
}
